package org.kompiro.jamcircle.storage.exception;

public class StorageConnectException extends Exception {

	private static final long serialVersionUID = 4966228349936188123L;

	public StorageConnectException() {
		super();
	}

	public StorageConnectException(String message, Throwable cause) {
		super(message, cause);
	}

	public StorageConnectException(String message) {
		super(message);
	}

	public StorageConnectException(Throwable cause) {
		super(cause);
	}


}
